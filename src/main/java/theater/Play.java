package theater;

/**
 * This class generate with palying name and type.
 *
 * @null this class does not accept null parameters
 */
public class Play {

    private String name;
    private String type;

    public Play(String name, String type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }
}
